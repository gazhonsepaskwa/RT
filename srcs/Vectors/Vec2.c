#include "../../includes/Vec.h"

t_v3	vec_sub(t_v3 a, t_v3 b)
{
	t_v3	res;

	res.x = a.x - b.x;
	res.y = a.y - b.y;
	res.z = a.z - b.z;
	return (res);
}
